/*
* [Day 005] Number type conversions
*
* */

fun main(args: Array<String>) {

    // to Byte
    println(5.toByte()) // 5

    // to Short
    println(5.5.toShort()) // 5

    // to Int
    println(10.5.toInt()) // 10

    // to Long
    println(3.toLong()) // 3

    // to Float
    println(7.toFloat()) // 7.0

    // to Double
    println(9.toDouble()) // 9.0

    // to Char
    println(10.toChar())

    // to String
    println(6.toString()) // 6
}