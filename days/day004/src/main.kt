/*
* [Day 004] Number Type Variables
*
* */

fun main(args: Array<String>) {

    // Short Int
    var wives: Short = 0
    println(wives) // 0

    // Int
    var ki: Int = 8000
    println(ki) // 8000

    // Int (more legible)
    ki = 8_000
    println(ki) // 8000

    // Long Int
    var stars: Long = 1_000_000_000_000_000_000
    println(stars) // 1000000000000000000

    // Double
    var salary: Double = 1000.00
    println(salary) // 1000.0

    // Float
    var height: Float = 1.73f
    println(height) // 1.73

}
